var bb=20;
console.log("Relational Operator > is:",aa>bb)
console.log("Relational Operator < is:",aa<bb)
console.log("Relational Operator <= is:",aa<=bb)
console.log("Relational Operator >= is:",aa>=bb)
console.log("Relational Operator === is:",aa===bb)
console.log("Relational Operator !== is:",aa!==bb)
